-- -----------------------------------------------------------------------------
-- Пример 1: Просмотр реплицируемых таблиц
-- Возвращает список всех таблиц, участвующих в подписках на данной реплике
-- -----------------------------------------------------------------------------
SELECT * FROM dba.get_replicated_tables();

-- -----------------------------------------------------------------------------
-- Пример 2: Полная проверка с точным COUNT (для базы mwallet)
-- Выполняет полный цикл: сбор логов + анализ алармов с порогом 50
-- Пароль хранится в ~/.pgpass (см. README) — в строке подключения не указывается
-- -----------------------------------------------------------------------------
SELECT * FROM dba.run_replication_check(
    'dbname=mwallet host=172.27.134.41 port=6432 user=dba'
);

-- -----------------------------------------------------------------------------
-- Пример 3: Проверка с приблизительным COUNT (для больших таблиц > 40 ГБ)
-- pg_class.reltuples возвращает приблизительное количество строк —
-- значительно быстрее точного COUNT(*) для больших таблиц
-- Пароль хранится в ~/.pgpass — в строке подключения не указывается
-- -----------------------------------------------------------------------------
SELECT * FROM dba.check_replication_counts(
    'dbname=ewallet host=172.27.134.41 port=6432 user=dba',
    TRUE   -- p_use_approximate = TRUE
);

-- -----------------------------------------------------------------------------
-- Пример 4: Проверка алармов за вчера с порогом 100
-- Ищет в dba.replication_log расхождения > 100 за вчерашний день
-- -----------------------------------------------------------------------------
SELECT * FROM dba.check_replication_alerts(100, CURRENT_DATE - 1);

-- -----------------------------------------------------------------------------
-- Пример 5: Просмотр логов за сегодня, отсортированных по размеру расхождения
-- -----------------------------------------------------------------------------
SELECT *
FROM dba.replication_log
WHERE check_date = CURRENT_DATE
ORDER BY ABS(count_diff) DESC;

-- -----------------------------------------------------------------------------
-- Пример 6: Просмотр последних 20 алармов
-- -----------------------------------------------------------------------------
SELECT *
FROM dba.replication_alerts
ORDER BY created_at DESC
LIMIT 20;

-- -----------------------------------------------------------------------------
-- Пример 7: Таблицы с наибольшими расхождениями за всё время
-- -----------------------------------------------------------------------------
SELECT
    db_name,
    schema_name,
    table_name,
    count_diff,
    check_date
FROM dba.replication_log
WHERE ABS(count_diff) > 0
ORDER BY ABS(count_diff) DESC;

-- -----------------------------------------------------------------------------
-- Пример 8: Проверка с явным порогом 100 и приблизительным COUNT
-- Полный цикл одной командой
-- Пароль хранится в ~/.pgpass — в строке подключения не указывается
-- -----------------------------------------------------------------------------
SELECT * FROM dba.run_replication_check(
    'dbname=mwallet host=172.27.134.41 port=6432 user=dba',
    100,   -- p_threshold — порог алармов
    TRUE   -- p_use_approximate — приблизительный COUNT для больших таблиц
);

-- -----------------------------------------------------------------------------
-- Пример 9: Статистика расхождений по базам за последние 7 дней
-- -----------------------------------------------------------------------------
SELECT
    db_name,
    COUNT(*)                              AS total_checks,
    COUNT(*) FILTER (WHERE ABS(count_diff) > 50)  AS checks_with_alerts,
    MAX(ABS(count_diff))                  AS max_diff,
    AVG(ABS(count_diff))::bigint          AS avg_diff
FROM dba.replication_log
WHERE check_date >= CURRENT_DATE - 7
GROUP BY db_name
ORDER BY max_diff DESC;
